export function computeResults(answers) {
  const sliderCats = ["initiative", "direction", "criteria", "change", "scope"];
  const sliderScores = {};

  for (const cat of sliderCats) {
    const qs = QUESTIONS.filter((q) => q.category === cat);
    const vals = qs.map((q) => answers[q.id] ?? 3);
    const avg = vals.reduce((a, b) => a + b, 0) / vals.length;
    let bucket;
    if (avg <= 2) bucket = "A";
    else if (avg >= 4) bucket = "B";
    else bucket = "M";
    sliderScores[cat] = { avg, bucket };
  }

  const channelCount = { visual: 0, auditory: 0, read: 0, kinesthetic: 0 };
  const q11picks = answers["q11"] || [];
  const q12picks = answers["q12"] || [];
  q11picks.forEach((k) => { channelCount[k] = (channelCount[k] || 0) + 2; });
  q12picks.forEach((k) => { channelCount[k] = (channelCount[k] || 0) + 1; });
  const sorted = Object.entries(channelCount).sort((a, b) => b[1] - a[1]);
  const top2 = [sorted[0][0], sorted[1][0]].sort();
  const channelKey = top2.join("+");
  const validChannelKey = CHANNEL_RESULTS[channelKey] ? channelKey : Object.keys(CHANNEL_RESULTS)[0];

  const collabCount = { solo: 0, proximity: 0, team: 0 };
  for (const qid of ["q13", "q14"]) {
    const pick = answers[qid];
    if (pick) collabCount[pick] = (collabCount[pick] || 0) + 1;
  }
  const collabSorted = Object.entries(collabCount).sort((a, b) => b[1] - a[1]);
  const collabType = collabSorted[0][0];

  return { sliderScores, channelKey: validChannelKey, channelCount, collabType, collabCount };
}

export function getSliderLabel(cat, bucket) {
  const m = CATEGORY_META[cat];
  if (bucket === "A") return m.labelA;
  if (bucket === "B") return m.labelB;
  return m.labelMid || "バランス型";
}
